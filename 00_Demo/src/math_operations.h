#ifndef MATH_OPERATIONS_H
#define MATH_OPERATIONS_H

/**
 * 两个整数相加
 * @param a 第一个数
 * @param b 第二个数
 * @return 相加结果
 */
int add(int a, int b);

/**
 * 两个整数相减
 * @param a 第一个数
 * @param b 第二个数
 * @return 相减结果
 */
int subtract(int a, int b);

/**
 * 两个整数相乘
 * @param a 第一个数
 * @param b 第二个数
 * @return 相乘结果
 */
int multiply(int a, int b);

/**
 * 两个整数相除
 * @param a 被除数
 * @param b 除数
 * @return 相除结果，如果除数为0返回0
 */
int divide(int a, int b);

#endif // MATH_OPERATIONS_H
